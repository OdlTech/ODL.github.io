    <h4 style="color: black;"><center><B><u>Frequently Asked Questions for Career Support </u></B></center></h4>
    
   <!--Content1  -->
    <div>
        <br>
        <br>
        <br>
    </div>
    <div class="container-xl">

        
        <div class="d-grid gap-2">
        
                <p>
                    <i class=" fa fa-question-circle me-4" style="color: black;"></i>
                    <button   class="btn btn-lg" type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample1" aria-expanded="true" aria-controls="collapseExample1" style="color:black; background-color: rgb(236, 236, 197);">
                        Is registration for placement mandatory for all eMasters students?
                    </button>
                </p>
                <div class="collapse" id="collapseExample1">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        The placement registration is NOT compulsory for all students. Only students who are seeking career opportunities and need the support of ODL should consider registering for placement. 
                    </div>
                </div>

            
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample2" aria-expanded="true" aria-controls="collapseExample2"  style="color:black; background-color: rgb(236, 236, 197);">
                        Is ODL placement the same as SPO placement by IIT K? 
                    </button>
                </p>
                <div class="collapse" id="collapseExample2">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        No, ODL operates independently of SPO, as the placement processes differ. SPO works through Campus placement, while ODL follows a lateral placement approach due to the diverse backgrounds and varied work experiences of its students. 
                    </div>
                </div>

            
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample3" aria-expanded="true" aria-controls="collapseExample3"  style="color:black; background-color: rgb(236, 236, 197);">
                        What are the various steps involved in the placement process for eMasters students?
                    </button>
                </p>
                <div class="collapse" id="collapseExample3">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        The process involves placement registration by the students, resume verification, company outreach, sharing student profiles, arranging interviews, and sharing final job offers. It is a student-driven process with different roles handled by eMasters students and the ODL placement team.
                    </div>
                </div>

            
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample5" aria-expanded="false" aria-controls="collapseExample5"  style="color:black; background-color: rgb(236, 236, 197);">
                        Who is eligible for placement registration?
                    </button>
                </p>
                <div class="collapse" id="collapseExample5">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        ODL team assesses students' eligibility for placement by ensuring that they are at their last quarter of the programme
                       
                        <ul>
                            <li>A student will be eligible for placement only if;</li>
                            <li>The student has completed twelve modules successfully and also extra/repeat or substitute modules they plan to complete during the programme. AND </li>
                            <li>The student completes all requirements for the degree within that quarter, i.e., Graduation is a prerequisite for students to be eligible for placement.</li>
                            Final eligibility will be determined, once a student has met all graduation requirements and their name is included in the graduation list.
                       </ul>
                       The registration is open for those who will potentially graduate from the programme by the end of the quarter when they complete their 12 courses. The registration for placement is open every quarter.
                    </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample12" aria-expanded="false" aria-controls="collapseExample12"  style="color:black; background-color: rgb(236, 236, 197);">
                        Is a company sponsored student eligible for placement registration?
                    </button>
                </p>
                <div class="collapse" id="collapseExample12">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        The ODL team requires a “No Objection Certificate” for any student sponsored/nominated by an organisation from the concerned authorities/agencies prior to being eligible for placement. 
                    </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample6" aria-expanded="false" aria-controls="collapseExample6"  style="color:black;background-color: rgb(236, 236, 197);">
                        When does the registration for placement process begin?
                    </button>
                </p>
                <div class="collapse" id="collapseExample6">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        Registration is open every quarter, and would begin within a week of the Last date for end quarter Grade submission (as mentioned in the eMasters Academic Calendar). The registration window would be open for approximately 3 weeks.   
                    </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample7" aria-expanded="false" aria-controls="collapseExample7"  style="color:black;background-color: rgb(236, 236, 197);">
                        What is the registration fee for placement?
                    </button>
                </p>
                <div class="collapse" id="collapseExample7">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        Currently, there is NO placement registration fee for eMasters students. However, registration is mandatory to access placement services. </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample8" aria-expanded="false" aria-controls="collapseExample8"  style="color:black;background-color: rgb(236, 236, 197);">
                        What is needed for Registration?
                    </button>
                </p>
                <div class="collapse" id="collapseExample8">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        Interested students must upload a master resume and supporting documents for verification. They are also required to submit their academic and professional details, along with a list of preferred companies for employment.
                        Additionally, students also upload a concise two-page resume, in a standard format provided by ODL. These will be shared with companies. The 2-page resume should be a subset of the master resume.
                         </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample9" aria-expanded="false" aria-controls="collapseExample9"  style="color:black;background-color: rgb(236, 236, 197);">
                        Is there a set format for the master resume?
                    </button>
                </p>
                <div class="collapse" id="collapseExample9">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        No, there is currently no set format or length or limit for the master resume. A student should add all the qualifications, experiences, skills and other relevant information which can enrich the CV as well as include all information of their profile. </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample10" aria-expanded="false" aria-controls="collapseExample10"  style="color:black;background-color: rgb(236, 236, 197);">
                        Is Registration necessary to be considered for placement?
                    </button>
                </p>
                <div class="collapse" id="collapseExample10">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        In order to be considered for placement, students must complete the "Student Placement Registration" forms.
                        The "Placement Registration" is completely different from "Academic Registration".
                    </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample11" aria-expanded="false" aria-controls="collapseExample11"  style="color:black;background-color: rgb(236, 236, 197);">
                        What is the validity of the placement registration?
                    </button>
                </p>
                <div class="collapse" id="collapseExample11">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        Students can participate in placement services extended by ODL only once, within one year of their graduation from IIT Kanpur. Placement registration would be valid for one year and one quarter after registration. One quarter would be used by the ODL placement team for the verification of documents and all internal processes.
                    </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample12" aria-expanded="false" aria-controls="collapseExample12"  style="color:black;background-color: rgb(236, 236, 197);">
                        Does the student have to register every quarter when placement registration opens?
                    </button>
                </p>
                <div class="collapse" id="collapseExample12">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        Once a student is eligible and completes the placement registration forms, the student does not have to register again if he is in the list of placement registered students (sent by OOA after registration is complete). The registration will be considered from the quarter he becomes eligible for placement. </div>
                </div>
                
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample14" aria-expanded="false" aria-controls="collapseExample14"  style="color:black;background-color: rgb(236, 236, 197);">
                        Does ODL inform students about the job openings before sharing the resumes with the companies?
                    </button>
                </p>
                <div class="collapse" id="collapseExample14">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        ODL in the registration form asks registered students to choose whether to share their resume with companies directly or with prior permission, ensuring control over resume distribution. Also, ODL shares the job description with the students and only forwards the resume if the student shows interest in seeking that job. </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample15" aria-expanded="false" aria-controls="collapseExample15"  style="color:black;background-color: rgb(236, 236, 197);">
                        How do the students support the placement?
                    </button>
                </p>
                <div class="collapse" id="collapseExample15">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        ODL seeks support from students through roles like Department Point of Contact (DPOC), Industry Coordinator (IC), and Social Media Coordinator (SMC). Final selections are made in consultation with existing student POCs and PCs. The responsibility associated with these positions are highlighted in a supportive google form which is shared with students twice a year or before a new programme is due for placement registration.</div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample16" aria-expanded="false" aria-controls="collapseExample16"  style="color:black;background-color: rgb(236, 236, 197);">
                        How long does the placement process take, and when can I expect to secure a job?
                    </button>
                </p>
                <div class="collapse" id="collapseExample16">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        ODL acts as a facilitator and counselor, not guaranteeing a job. The process duration varies, with some securing offers soon after completion, while others take months post-graduation or through independent sources. </div>
                </div>
                <p>
                    <i class="fa fa-question-circle me-4" style="color: black; size: 10px;"></i>
                    <button  class="btn btn-lg " type="button" data-bs-toggle="collapse" data-bs-target="#collapseExample17" aria-expanded="false" aria-controls="collapseExample17"  style="color:black;background-color: rgb(236, 236, 197);">
                        Can a student with a job through ODL/IITK sit for placement for another job?
                    </button>
                </p>
                <div class="collapse" id="collapseExample17">
                    <div class="card card-body" style="color:black; background-color: rgb(203, 223, 231);">
                        ODL follows a one-student-one-job policy. Refer to the Placement Policy at the student portal for more details.
                    </div>
                </div>
               

          
